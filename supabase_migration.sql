-- Профили пользователей (связан с auth.users)
CREATE TABLE IF NOT EXISTS public.profiles (
  id uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  email text NOT NULL,
  full_name text,
  role text NOT NULL DEFAULT 'user' CHECK (role IN ('user', 'moderator', 'admin')),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

-- Дополнительные поля профиля (для блокировок и расширенного функционала)
ALTER TABLE public.profiles
  ADD COLUMN IF NOT EXISTS is_blocked boolean NOT NULL DEFAULT false;

CREATE UNIQUE INDEX IF NOT EXISTS profiles_email_key ON public.profiles (email);

COMMENT ON TABLE public.profiles IS 'Профили пользователей, привязанные к auth.users';
COMMENT ON COLUMN public.profiles.role IS 'Роль пользователя: user / moderator / admin';
COMMENT ON COLUMN public.profiles.is_blocked IS 'Флаг блокировки пользователя (true = вход запрещён)';

-- Представление для модераторов
CREATE OR REPLACE VIEW public.moderators AS
  SELECT * FROM public.profiles WHERE role = 'moderator';

-- Представление для администраторов
CREATE OR REPLACE VIEW public.admins AS
  SELECT * FROM public.profiles WHERE role = 'admin';

COMMENT ON VIEW public.moderators IS 'Все пользователи с ролью moderator';
COMMENT ON VIEW public.admins IS 'Все пользователи с ролью admin';

-- Заявки на регистрацию (для страницы регистрации и модерации)
CREATE TABLE IF NOT EXISTS public.registration_requests (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email text NOT NULL,
  full_name text,
  role_requested text NOT NULL DEFAULT 'user' CHECK (role_requested IN ('user', 'moderator', 'admin')),
  status text NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'approved', 'rejected')),
  created_at timestamptz NOT NULL DEFAULT now(),
  processed_at timestamptz
);

CREATE INDEX IF NOT EXISTS registration_requests_email_pending_idx
  ON public.registration_requests (email)
  WHERE status = 'pending';

COMMENT ON TABLE public.registration_requests IS 'Заявки с формы регистрации, которые могут одобрять модераторы/админы';
COMMENT ON COLUMN public.registration_requests.role_requested IS 'Желаемая роль: user / moderator / admin';
COMMENT ON COLUMN public.registration_requests.status IS 'Статус заявки: pending / approved / rejected';

