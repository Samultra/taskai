-- Отделы / команды
CREATE TABLE IF NOT EXISTS public.departments (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  description text,
  parent_id bigint REFERENCES public.departments(id) ON DELETE SET NULL,
  created_at timestamptz NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.departments IS 'Отделы и команды компании (иерархическая структура)';

-- Проекты
CREATE TABLE IF NOT EXISTS public.projects (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  code text,
  description text,
  color text,
  owner_profile_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  is_archived boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.projects IS 'Проекты, которыми управляет система TaskAI';

-- Участники проектов
CREATE TABLE IF NOT EXISTS public.project_members (
  project_id bigint NOT NULL REFERENCES public.projects(id) ON DELETE CASCADE,
  profile_id uuid NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  role text NOT NULL DEFAULT 'member',
  added_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (project_id, profile_id)
);

COMMENT ON TABLE public.project_members IS 'Связь пользователей и проектов (участники проектов)';

-- Привязка проектов к отделам
CREATE TABLE IF NOT EXISTS public.project_departments (
  project_id bigint NOT NULL REFERENCES public.projects(id) ON DELETE CASCADE,
  department_id bigint NOT NULL REFERENCES public.departments(id) ON DELETE CASCADE,
  PRIMARY KEY (project_id, department_id)
);

COMMENT ON TABLE public.project_departments IS 'Какие отделы/команды отвечают за какие проекты';
